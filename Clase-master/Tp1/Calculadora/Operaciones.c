
int suma(int a,int b)
{
    int resultado;
    resultado = a + b;
    return resultado;
}

int resta(int a, int b)
{
    int resultado;
    resultado = a - b;
    return resultado;
}
int multiplicacion(int a, int b)
{
    int resultado;
    resultado = a * b;
    return resultado;
}
float divicion(int a, int b)
{
    float resultado;
    resultado = (float)a / b;
    return resultado;
}
